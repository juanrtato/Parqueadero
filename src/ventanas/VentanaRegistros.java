    void mostrarTabla(){
        Conectar conexion = new Conectar();
        Connection reg = conexion.getConnection();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("id_ocup");
            modelo.addColumn("placa_ocup");
            modelo.addColumn("num_plaza_ocup");
            modelo.addColumn("color_ocup");
            modelo.addColumn("marca_ocup");
            modelo.addColumn("descrip_ocup");
            modelo.addColumn("fecha_ingreso");
            modelo.addColumn("fecha_salida");
            tablaRegistros.setModel(modelo);
            
            String sql = "SELECT * FROM ocupante";
            String datos [] = new String[9];
            Statement st;
        try {
            st = reg.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                modelo.addRow(datos);
            }
            tablaRegistros.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(VentanaRegistros.class.getName()).log(Level.SEVERE, null, ex);
        }
        conexion.desconectar();
            
    }
