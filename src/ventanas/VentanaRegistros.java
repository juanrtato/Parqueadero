    private void docExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_docExcelActionPerformed
        if(tablaRegistros.getColumnCount()!=0){
        try{
        String file  = "dataParkingUIS..xls";
        TableModel model = tablaRegistros.getModel();
        FileWriter excel = new FileWriter(file);

        for(int i = 0; i < model.getColumnCount(); i++){
            excel.write(model.getColumnName(i) + "\t");
        }

        excel.write("\n");

        for(int i=0; i< model.getRowCount(); i++) {
            for(int j=0; j < model.getColumnCount(); j++) {
                excel.write(model.getValueAt(i,j).toString()+"\t");
            }
            excel.write("\n");
        }

        excel.close();
        JOptionPane.showMessageDialog(null, "Archivo excel creado con Ã©xito");
        Desktop.getDesktop().open(new File("dataParkingUIS..xls"));
        }catch(IOException e){ System.out.println(e); }
        }
        else {
            JOptionPane.showMessageDialog(null, "No hay datos en la tabla para crear un archivo excel");
        }
    }//GEN-LAST:event_docExcelActionPerformed

